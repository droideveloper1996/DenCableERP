package com.capiyoo.dencables.Utilities;

public class PlansPricing {

    private String mPlanName;
    private String mPlanBasePrice;
    private String mPlanDescription;
    private String mUsageDetail;


    private String mAdvantage;
    private String mPlanServices;

    public String getmPlanName() {
        return mPlanName;
    }

    public void setmPlanName(String mPlanName) {
        this.mPlanName = mPlanName;
    }

    public String getmPlanBasePrice() {
        return mPlanBasePrice;
    }

    public void setmPlanBasePrice(String mPlanBasePrice) {
        this.mPlanBasePrice = mPlanBasePrice;
    }

    public String getmPlanDescription() {
        return mPlanDescription;
    }

    public void setmPlanDescription(String mPlanDescription) {
        this.mPlanDescription = mPlanDescription;
    }

    public String getmUsageDetail() {
        return mUsageDetail;
    }

    public void setmUsageDetail(String mUsageDetail) {
        this.mUsageDetail = mUsageDetail;
    }

    public String getmAdvantage() {
        return mAdvantage;
    }

    public void setmAdvantage(String mAdvantage) {
        this.mAdvantage = mAdvantage;
    }

    public String getmPlanServices() {
        return mPlanServices;
    }

    public void setmPlanServices(String mPlanServices) {
        this.mPlanServices = mPlanServices;
    }
}
